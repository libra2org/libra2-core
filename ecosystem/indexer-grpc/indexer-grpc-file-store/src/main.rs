use anyhow::Result;
use libra2_indexer_grpc_file_store::IndexerGrpcFileStoreWorkerConfig;
use libra2_indexer_grpc_server_framework::ServerArgs;
use clap::Parser;

#[cfg(unix)]
#[global_allocator]
static ALLOC: jemallocator::Jemalloc = jemallocator::Jemalloc;

#[tokio::main]
async fn main() -> Result<()> {
    let args = ServerArgs::parse();
    args.run::<IndexerGrpcFileStoreWorkerConfig>()
        .await
        .expect("Failed to run server");
    Ok(())
}
